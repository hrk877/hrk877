function getRandomCombination(intros: string[], outros: string[]): string {
    const intro = intros[Math.floor(Math.random() * intros.length)];
    const outro = outros[Math.floor(Math.random() * outros.length)];
    return `${intro}\n${outro}`;
}

export function getRandomBananaMessage(): string {
    // 5 Categories x 10 Intros x 10 Outros = 500 Combinations.
    // To reach 1000+, we can simply weigh them or just accept 500 high-quality ones.
    // Or, we can blindly mix Intros/Outros across COMPATIBLE categories.
    // Time/Structure/Power/Diverse/Play are sufficiently distinct that crossing them might look weird,
    // but Structure and Power often mix well.
    // Let's stick to strict categories to ensure logical flow, but pick a random category.

    const categories = [
        { i: CAT_TIME_INTROS, o: CAT_TIME_OUTROS },
        { i: CAT_STRUCT_INTROS, o: CAT_STRUCT_OUTROS },
        { i: CAT_POWER_INTROS, o: CAT_POWER_OUTROS },
        { i: CAT_DIVERSE_INTROS, o: CAT_DIVERSE_OUTROS },
        { i: CAT_PLAY_INTROS, o: CAT_PLAY_OUTROS }
    ];

    const cat = categories[Math.floor(Math.random() * categories.length)];
    return getRandomCombination(cat.i, cat.o);
}
